const estadosController ={};
const oracledb = require('oracledb');
const dbConfig = require('../utils/dbconfig');

estadosController.obtenerEstadosPedidos = async (req, res) => {
    try {
        //Hacemos la conexion
        const connection = await oracledb.getConnection(dbConfig);

        //Secuencia sql 
        const secuenciaSQL= `SELECT * FROM TBL_ESTADOS_PEDIDO
                            WHERE ID_ESTADO IN (1,5,8)`;

        //para que devuelva en JSON los rows
        const options= {
            outFormat: oracledb.OUT_FORMAT_OBJECT,
        };

        const result = await connection.execute(secuenciaSQL,[],options);

        // Cerrar la conexión después de obtener los datos
        await connection.close(); 

        // Enviar los datos
        res.send(result.rows); 

    } catch (error) {

        res.status(500).send({ error: error.message }); 
    }
}


module.exports = estadosController;